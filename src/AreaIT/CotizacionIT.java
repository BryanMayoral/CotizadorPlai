    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        try {
            
            int Tiempo_de_almacenamiento = Integer.parseInt(txtTA.getText().trim());
            int Cantidad_de_cursos = Integer.parseInt(txtCC.getText().trim());
            int Cantidad_de_operadores = Integer.parseInt(txtOP.getText().trim());
            String Cliente=txtCliente.getText().trim();
            
            
            if(validarNegativo()){
                JOptionPane.showMessageDialog(null,"No se puede poner numeros negativos","Valor invalido",JOptionPane.ERROR_MESSAGE);
                return;
            }

            float Cotizacion = Tiempo_de_almacenamiento * ((Cantidad_de_cursos * Costo_por_almacenamiento) + ((Cantidad_de_cursos / 30.0f) * Costo_de_soporte)) + (Cantidad_de_operadores * Cantidad_de_cursos * Costo_por_gestion);

            subirInfo(Tiempo_de_almacenamiento, Cantidad_de_cursos, Cantidad_de_operadores, Cotizacion, Cliente);
            AclararCampos();
            JOptionPane.showMessageDialog(this, "La cotización es: " + Cotizacion);
        } catch(NumberFormatException e) {
            if(validarCampoVacio()){
                JOptionPane.showMessageDialog(null,"Tiene que rellenar los campos en rojo.","Campos vacios",JOptionPane.ERROR_MESSAGE);
            }else if(validarCaracter()){
                JOptionPane.showMessageDialog(null,"Los campos tienen que llenarse con numeros enteros. No se permiten caracteres ni numeros con comas","Valor invalido",JOptionPane.ERROR_MESSAGE);
            }        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error de conexion, intente de nuevo","Fail Coneccion",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

