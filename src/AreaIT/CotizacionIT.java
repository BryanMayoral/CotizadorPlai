    public static boolean validarCampoVacio(){
        AclararCampos();
        boolean x=false;
        for(JTextField obj:ListaText){
            if(obj.getText().trim().isEmpty()){
                obj.setBackground(Color.red);
                x=true;
            }
        }
        return x;
    }
    
